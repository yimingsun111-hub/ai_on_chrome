const KEY = "uiLang";

export async function loadLang() {
  const d = await chrome.storage.local.get(KEY);
  return d[KEY] || null; // null = 从未选择（触发首次选择界面）
}

export async function saveLang(id) {
  await chrome.storage.local.set({ [KEY]: id });
}

// 根据浏览器语言猜一个默认值
export function detectDefault() {
  const nav = (navigator.language || "en").toLowerCase();
  for (const l of LANGUAGES) if (l.id.toLowerCase() === nav) return l.id;
  for (const l of LANGUAGES) if (l.id.split("-")[0] === nav.split("-")[0]) return l.id;
  return "en";
}

let current = "zh-CN";
export function setCurrent(id) {
  if (LANGUAGES.some((l) => l.id === id)) current = id;
}
export function getCurrent() { return current; }

export function t(key, ...args) {
  const s = STRINGS[current]?.[key] ?? STRINGS.en[key] ?? STRINGS["zh-CN"][key] ?? key;
  return typeof s === "function" ? s(...args) : s;
}

